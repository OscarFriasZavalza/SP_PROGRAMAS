;4.1.2- Sumar tres números y almacenar los números y el resultado en las 
;direcciones 0x0c 0x0d 0x0e 0x0f
LIST P=16F84
VAR1 EQU 0X0C
VAR2 EQU 0X0D
VAR3 EQU 0X0E
RES1 EQU 0X0F

ORG 0
;INICIO
INICIO

MOVLW D'1';1->W
MOVWF VAR1;W->VAR1
MOVLW D'2';2->W
MOVWF VAR2;W->VAR2
MOVLW D'3';3->W
MOVWF VAR3;W->VAR3
CLRW;LIMPIAMOS 2
MOVF VAR3,0;VAR->W
ADDWF VAR2,0;W+VAR2
ADDWF VAR1,0;W+VAR1
MOVWF RES1;RESULTADO=6

END