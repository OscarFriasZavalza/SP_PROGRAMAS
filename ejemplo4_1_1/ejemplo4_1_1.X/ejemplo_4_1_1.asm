;SUMAR CUATRO NUMEROS EN BINARIO 0,1,2,3
    LIST P=16F84
    SUMA1 EQU 0X0C
    SUMA2 EQU 0X01
    RESUL EQU 0X0D
 
    ORG 0
    
    MOVLW b'0000'  ; PONEMOS 2 EN W
    MOVWF SUMA1 ; PASAMOS W A SUMA 1
    MOVLW b'0001' ;PONEMOS 3 EN W
    ADDWF SUMA1,1 ;SUMAMOS W COM SUMA 1 Y RESULTADO EN W
    ;HASTA ESTE PUNTO YA TENEMOS LA SUMA DE 2 NUEMROS
    MOVLW b'0010' ; W->0001
    MOVWF SUMA2 ;SUMA2->W
    MOVLW B'0011';W->0100
    ADDWF SUMA2,0;W+SUMA2 RESULTADO EN W
    ADDWF SUMA1,0;W+SUMA1 RESULTADO EN W
    MOVWF RESUL; PASAMOS W->F RESUL=6
    END